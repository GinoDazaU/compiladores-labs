// AssignStm
// =============================================================================

AssignStm::AssignStm(const std::string& variable, Exp* expresion)
    : id(variable), e(expresion) {}

AssignStm::~AssignStm() {}

// =============================================================================
// PrintStm
// =============================================================================

PrintStm::PrintStm(Exp* expresion) : e(expresion) {}

