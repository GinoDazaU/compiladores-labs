// NumberExp
// =============================================================================

NumberExp::NumberExp(int v) : value(v) {}

NumberExp::~NumberExp() {}

// =============================================================================
// IdExp
// =============================================================================

IdExp::IdExp(const std::string& v) : value(v) {}

IdExp::~IdExp() {}

