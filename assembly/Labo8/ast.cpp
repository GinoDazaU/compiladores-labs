#include "ast.h"
#include <iostream>

// =============================================================================
// Exp
// =============================================================================

Exp::~Exp() {}

std::string Exp::binopToChar(BinaryOp op) {
    switch (op) {
        case PLUS_OP:  return "+";
        case MINUS_OP: return "-";
        case MUL_OP:   return "*";
        case DIV_OP:   return "/";
        case POW_OP:   return "**";
        case LE_OP:    return "<";
        default:       return "?";
    }
}

// =============================================================================
// BinaryExp
// =============================================================================

BinaryExp::BinaryExp(Exp* l, Exp* r, BinaryOp o)
    : left(l), right(r), op(o) {}

BinaryExp::~BinaryExp() {
    delete left;
    delete right;
}

